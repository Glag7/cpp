#include <iostream>
#include "Span.hpp"

int	main(void)
{
	{
		Span sp = Span(5);
		sp.addNumber(6);
		sp.addNumber(3);
		sp.addNumber(17);
		sp.addNumber(9);
		sp.addNumber(11);
		std::cout << sp.shortestSpan() << std::endl;
		std::cout << sp.longestSpan() << std::endl;
	}
	{
		Span			s(100000);
		std::vector<int>	vec;

		for (int i = 0; i < 100000; ++i)
			vec.push_back(i);
		s.addNumber(vec.begin(), vec.end());
		std::cout << s.shortestSpan() << std::endl;
		std::cout << s.longestSpan() << std::endl;
	}
}
